#!/usr/bin/env node
/**
 * Fetches TMDB poster_path for each Best Picture winner/nominee by TITLE and YEAR
 * so posters match the truth list (ids in the list may not match TMDB movie ids).
 * Writes lib/bestPicturePosters.data.ts.
 *
 * Usage: TMDB_API_KEY=yourkey node scripts/fetch-best-picture-posters.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
try {
  const env = readFileSync(join(root, '.env.local'), 'utf8');
  for (const line of env.split('\n')) {
    const m = line.match(/^\s*TMDB_API_KEY\s*=\s*(.+)/);
    if (m) process.env.TMDB_API_KEY = m[1].trim().replace(/^["']|["']$/g, '');
  }
} catch {
  // .env.local optional
}
const apiKey = process.env.TMDB_API_KEY;
if (!apiKey) {
  console.error('Set TMDB_API_KEY in .env.local or env');
  process.exit(1);
}

/** Extract { id, title, year } from a .ts file that has lines like { id: 123, title: "Foo", year: 2024 } */
function extractEntriesFromTs(filePath) {
  const text = readFileSync(join(root, filePath), 'utf8');
  const entries = [];
  const re = /\{\s*id:\s*(\d+)\s*,\s*title:\s*["']((?:[^"']|\\.)*)["']\s*,\s*year:\s*(\d+)/g;
  let m;
  while ((m = re.exec(text)) !== null) {
    entries.push({ id: parseInt(m[1], 10), title: m[2].replace(/\\./g, (c) => c), year: parseInt(m[3], 10) });
  }
  return entries;
}

const winnerEntries = extractEntriesFromTs('lib/bestPictureData.ts');
const nomineeEntries = extractEntriesFromTs('lib/academyAwardData.ts');
const byKey = new Map();
for (const e of [...winnerEntries, ...nomineeEntries]) {
  byKey.set(`${e.id}`, e);
}
const entries = [...byKey.values()];
console.log(`Resolving posters for ${entries.length} movies by title+year...`);

const out = {};
for (let i = 0; i < entries.length; i++) {
  const { id: ourId, title, year } = entries[i];
  try {
    const byIdRes = await fetch(
      `https://api.themoviedb.org/3/movie/${ourId}?api_key=${apiKey}`
    );
    const byIdData = await byIdRes.json();
    const byIdTitle = (byIdData.title || '').trim().toLowerCase();
    const byIdYear = byIdData.release_date ? parseInt(byIdData.release_date.slice(0, 4), 10) : 0;
    const ourTitleNorm = title.trim().toLowerCase();
    const titleMatches = byIdTitle === ourTitleNorm || byIdTitle.includes(ourTitleNorm) || ourTitleNorm.includes(byIdTitle);
    const yearMatches = byIdYear === year;
    if (titleMatches && yearMatches && (byIdData.poster_path != null)) {
      out[ourId] = byIdData.poster_path;
    } else {
      const searchRes = await fetch(
        `https://api.themoviedb.org/3/search/movie?api_key=${apiKey}&query=${encodeURIComponent(title)}&primary_release_year=${year}`
      );
      const searchData = await searchRes.json();
      const results = searchData.results || [];
      const first = results[0];
      out[ourId] = first && first.poster_path ? first.poster_path : null;
      if (!first && byIdData.poster_path) out[ourId] = byIdData.poster_path;
    }
    if ((i + 1) % 10 === 0) console.log(`  ${i + 1}/${entries.length}`);
  } catch (e) {
    console.warn(`  Failed ${ourId} ${title} (${year}):`, e.message);
    out[ourId] = null;
  }
}

const lines = Object.entries(out)
  .sort(([a], [b]) => Number(a) - Number(b))
  .map(([id, path]) => `  ${id}: ${path ? `"${path.replace(/"/g, '\\"')}"` : 'null'}`);
const content = `/**
 * TMDB poster_path for Best Picture winners and nominees.
 * Resolved by title+year so posters match the truth list. Generated by: npm run fetch-best-picture-posters
 */
export const BEST_PICTURE_POSTER_PATHS: Record<number, string | null> = {
${lines.join(',\n')},
};
`;
writeFileSync(join(root, 'lib/bestPicturePosters.data.ts'), content, 'utf8');
console.log('Wrote lib/bestPicturePosters.data.ts');
